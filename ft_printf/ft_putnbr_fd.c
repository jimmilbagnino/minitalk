#include "ft_printf.h"

void	ft_putnbr_fd(int n, int fd)
{
	long int	g;

	g = n;
	if (g < 0)
	{
		ft_putchar_fd('-', fd);
		g *= -1;
	}
	if (g > 9)
	{
		ft_putnbr_fd(g / 10, fd);
	}
	ft_putchar_fd(g % 10 + '0', fd);
}
