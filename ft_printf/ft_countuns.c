#include "ft_printf.h"

int	ft_countuns(unsigned int n)
{	
	unsigned long int	g;
	int					i;

	g = n;
	i = 0;
	if (g == 0)
		return (1);
	while (n > 0)
	{
		i++;
		n = n / 10;
	}
	return (i);
}
